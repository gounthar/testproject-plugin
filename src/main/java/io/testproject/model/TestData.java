package io.testproject.model;

/**
 * Tests response data
 */
public class TestData {
    /**
     * The test's Id
     */
    private String id;

    /**
     * The test's name
     */
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
